drop database if exists vk;
create database vk;

use vk;

drop table if exists users;
create table users(
	id SERIAL primary key,
	firstname varchar(50),
	lastname varchar(50),
	email varchar(100) UNIQUE,
	phone BIGINT,
	index users_phone_idx(phone),
	index users_names_idx(firstname,lastname)
);

drop table if exists profiles;
create table profiles (
	user_id bigint unsigned not null primary key,
	gender char(1),
	birthday datetime,
	photo_id bigint unsigned null,
	created_at datetime default now(),
	hometown varchar(100),
	foreign key (user_id) references users(id)
);

drop table if exists messages;
create table messages (
	id SERIAL primary key,
	from_user_id bigint unsigned not null,
	to_user_id bigint unsigned not null,
	body text,
	created_at datetime default now(),
	index(from_user_id),
	index(to_user_id),
	foreign key (from_user_id) references users(id),
	foreign key (to_user_id) references users(id)
);

drop table if exists friend_requests;
create table friend_requests (
	initiator_user_id bigint unsigned not null,
	target_user_id bigint unsigned not null,
	status enum('requested', 'approved', 'decline', 'unfriended') default 'requested',
	requested_at datetime default now(),
	confirmed_at datetime,
	primary key(initiator_user_id,target_user_id),
	index(initiator_user_id),
	index(target_user_id),
	foreign key (initiator_user_id) references users(id),
	foreign key (target_user_id) references users(id)
);

drop table if exists communites;
create table communites (
	id SERIAL primary key,
	name varchar(150),
	index communites_name_idx(name)
);


drop table if exists users_communites;
create table users_communites (
	user_id bigint unsigned not null,
	community_id bigint unsigned not null,
	primary key(user_id,community_id),
	foreign key (user_id) references users(id),
	foreign key (community_id) references communites(id)
);
	
DROP TABLE IF EXISTS media_types;
CREATE TABLE media_types(
	id SERIAL PRIMARY KEY,
    name VARCHAR(255),
    created_at DATETIME DEFAULT NOW(),
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
 ); 

DROP TABLE IF EXISTS media;
CREATE TABLE media(
	id SERIAL PRIMARY KEY,
    media_type_id BIGINT UNSIGNED NOT NULL,
    user_id BIGINT UNSIGNED NOT NULL,
  	body text,
    filename VARCHAR(255),
    size INT,
	metadata JSON,
    created_at DATETIME DEFAULT NOW(),
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

    INDEX (user_id),
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (media_type_id) REFERENCES media_types(id)
);

DROP TABLE IF EXISTS media_likes;
CREATE TABLE media_likes(
    user_id BIGINT UNSIGNED NOT NULL,
    media_id BIGINT UNSIGNED NOT NULL,
    created_at DATETIME DEFAULT NOW(),
    primary key(user_id, media_id),
	FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (media_id) REFERENCES media(id)
);

DROP TABLE IF EXISTS photo_albums;
CREATE TABLE photo_albums (
	id SERIAL,
	name varchar(255) DEFAULT NULL,
    user_id BIGINT UNSIGNED DEFAULT NULL,

    FOREIGN KEY (user_id) REFERENCES users(id),
  	PRIMARY KEY (id)
);

DROP TABLE IF EXISTS photos;
CREATE TABLE photos (
	id SERIAL PRIMARY KEY,
	album_id BIGINT unsigned NOT NULL,
	media_id BIGINT unsigned NOT NULL,
	FOREIGN KEY (album_id) REFERENCES photo_albums(id),
    FOREIGN KEY (media_id) REFERENCES media(id)
);


DROP TABLE IF EXISTS photo_likes;
CREATE TABLE photo_likes(
    user_id BIGINT UNSIGNED NOT NULL,
    photo_id BIGINT UNSIGNED NOT NULL,
    created_at DATETIME DEFAULT NOW(),
	FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (photo_id) REFERENCES photos(id),
    primary key(user_id, photo_id)
);

DROP TABLE IF EXISTS profile_likes;
CREATE TABLE profile_likes(
    user_id BIGINT UNSIGNED NOT NULL,
    profile_id BIGINT UNSIGNED NOT NULL,
    created_at DATETIME DEFAULT NOW(),
	FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (profile_id) REFERENCES profiles(user_id),
    primary key(user_id, profile_id)
);

